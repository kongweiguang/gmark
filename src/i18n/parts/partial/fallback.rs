macro_rules! merge_i18n_strings {
    ($source:ident, $defaults:ident; $($field:ident),+ $(,)?) => {{
        let slash_commands = match $source.slash_commands {
            Some(custom) => {
                let mut merged = $defaults.slash_commands;
                merged.extend(custom);
                merged
            }
            None => $defaults.slash_commands,
        };
        I18nStrings {
            $($field: $source.$field.unwrap_or($defaults.$field),)+
            slash_commands,
        }
    }};
}

